
// for반복문

let sum = 0;
for (let i=1; i<=10; i++) {
    console.log(`${i}`);
    sum += i;
}
console.log(`${sum}`);

console.log("---------------------------------");

// 10부터 1까지의 반대로 반복문의 출력

let sum2 = 0;
for(let j = 10; j >= 1; j--) {
    console.log(`${j}`);
    sum2 += j;
}
console.log(`${sum2}`);